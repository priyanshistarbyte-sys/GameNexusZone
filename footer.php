		<script>
			let games = [];
			let favorites = JSON.parse(localStorage.getItem('favorites')) || [];
			let displayedGames = 12;
			let currentCategory = 'all';
			let sortBy = 'rating';
			let searchTerm = '';

			async function loadGames() {
				try {
					const response = await fetch('data.json');
					const gameData = await response.json();
					games = gameData.map((game, index) => ({
						id: index + 1,
						title: game.name.replace(/-/g, ' ').replace(/\b\w/g, l => l.toUpperCase()),
						category: game.category.split(',')[0],
						image: ROOT_URL + game.img,
						rating: (Math.random() * 1.5 + 3.5).toFixed(1),
						players: Math.floor(Math.random() * 2000 + 100),
						featured: game.category.includes('popular'),
						isNew: Math.random() > 0.7,
						url: ROOT_URL + game.url
					}));
					renderGames();
				} catch (error) {
					console.error('Error loading games:', error);
				}
			}

			function renderGames() {
				const gamesGrid = document.getElementById('games-grid');
				if (!gamesGrid) return;

				let filtered = games.filter(game => {
					const matchCat = currentCategory === 'all' || game.category === currentCategory;
					const matchSearch = searchTerm === '' || game.title.toLowerCase().includes(searchTerm);
					return matchCat && matchSearch;
				});

				filtered.sort((a, b) => b.rating - a.rating);
				const toShow = filtered.slice(0, displayedGames);

				gamesGrid.innerHTML = toShow.map(game => `
					<div class="game-card" onclick="window.open('${game.url}', '_blank')">
						<div style="position:relative;overflow:hidden">
							<img src="${game.image}" alt="${game.title}" class="card-img">
							<div style="position:absolute;inset:0;background:linear-gradient(to top,rgba(15,15,26,0.85) 0%,transparent 50%);opacity:0;transition:opacity 0.3s" class="card-overlay"></div>
							<div style="position:absolute;top:10px;left:10px;display:flex;gap:6px">
								${game.isNew ? '<span class="badge-new">New</span>' : ''}
								${game.featured ? '<span class="badge-top">⭐ Top</span>' : ''}
							</div>
						</div>
						<div style="padding:14px">
							<h3 style="font-size:0.9rem;font-weight:700;color:#f1f5f9;margin-bottom:8px;white-space:nowrap;overflow:hidden;text-overflow:ellipsis">${game.title}</h3>
							<div style="display:flex;justify-content:space-between;align-items:center;margin-bottom:12px">
								<span style="color:#fbbf24;font-size:0.8rem;font-weight:600">★ ${game.rating}</span>
								<span style="color:#6b7280;font-size:0.75rem">${game.players.toLocaleString()} plays</span>
							</div>
							<button class="play-btn">▶ Play Now</button>
						</div>
					</div>
				`).join('');

				// hover overlay effect
				document.querySelectorAll('.game-card').forEach(card => {
					const overlay = card.querySelector('.card-overlay');
					card.addEventListener('mouseenter', () => { if(overlay) overlay.style.opacity = '1'; });
					card.addEventListener('mouseleave', () => { if(overlay) overlay.style.opacity = '0'; });
				});
			}

			document.addEventListener('DOMContentLoaded', function() {
				loadGames();
				const loadMoreBtn = document.getElementById('load-more');
				if (loadMoreBtn) {
					loadMoreBtn.addEventListener('click', function() {
						displayedGames += 8;
						renderGames();
					});
				}
			});

			function filterGames() { renderGames(); }
		</script>

